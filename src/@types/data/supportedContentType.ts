/**
 * All of the content types that Polaroids recognizes. For further extension,
 * look up common MIME types, and cross reference them with PhotoStation6
 * supported types.
 */
export enum SupportedContentType {
  JPG = "image/jpeg",
  TIFF = "image/tiff",
  PNG = "image/png",
  GIF = "image/gif",
  MP4 = "video/mp4",
  MPEG = "video/mpeg",
  MOV = "video/mov",
}
